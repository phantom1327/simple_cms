    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Employee $employee)
    {
        $employee->delete();
        return redirect()->route('employee.index');
    }

    public function list(Request $request){
        if($request->ajax()){
            return DataTables::eloquent(Employee::with('company'))->addColumn('actions', function($row){
                return \Form::open(['url' => route('employee.destroy', $row), 'method' => 'delete']).\Form::submit('Delete!').\Form::close();
            })->rawColumns(['actions'])->toJson();
        }
    }


